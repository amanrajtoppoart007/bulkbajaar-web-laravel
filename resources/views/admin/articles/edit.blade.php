    Dropzone.options.primaryImageDropzone = {
    url: '{{ route('admin.articles.storeMedia') }}',
    maxFilesize: 2, // MB
    acceptedFiles: '.jpeg,.jpg,.png,.gif',
    maxFiles: 1,
